BENEFITS_STOPWORDS = {
    "的", "和", "与", "或", "及", "等", "是", "有", "无", "不限",
    "薪资", "待遇", "福利", "面议", "良好", "优厚", "丰厚",
    "提供", "公司", "岗位", "要求", "以及", "年度", "保障",
    "齐全", "完善", "标准", "行业", "对应", "方面", "该", "上",
    "给", "做", "为", "以", "从", "到", "了", "在", "也",
    "都", "还", "就", "对", "让", "月",
}


def register_benefits_dict():
    """向 jieba 注册所有自定义福利词汇"""
    for w in BENEFITS_CUSTOM_WORDS:
        jieba.add_word(w)


def extract_benefits_keywords(benefits_text: str, top_n: int = 120):
    """
    从福利文本中提取关键词及词频

    参数:
        benefits_text: 拼接后的福利文本字符串
        top_n: 返回TOP N 词频结果
    返回:
        list of (word, frequency) tuples
    """
    register_benefits_dict()
    segments = benefits_text.replace("/", " ").replace("、", " ").replace("，", " ").replace("+", " ")
    words = jieba.cut(segments)
    word_list = [w.strip() for w in words if len(w.strip()) >= 2]
    word_list = [w for w in word_list if w not in BENEFITS_STOPWORDS]
    word_freq = Counter(word_list)
    return word_freq.most_common(top_n)
